jQuery(document).ready(function () {

	var iRegion = 0;
	var htmlRegion = '<option value="sin-region">Seleccione Región</option><option value="sin-region">--</option>';
	var htmlCiudades = '<option value="sin-region">Seleccione Ciudad</option><option value="sin-region">--</option>';

	jQuery.each(RegionesYciudades.regiones, function () {
		htmlRegion = htmlRegion + '<option value="' + RegionesYciudades.regiones[iRegion].NombreRegion + '">' + RegionesYciudades.regiones[iRegion].NombreRegion + '</option>';
		iRegion++;
	});

	jQuery('#regiones').html(htmlRegion);
	jQuery('#Ciudades').html(htmlCiudades);

	jQuery('#regiones').change(function () {
		var iRegiones = 0;
		var valorRegion = jQuery(this).val();
		var htmlCiudad = '<option value="sin-ciudad">Seleccione Ciudad</option><option value="sin-ciudad">--</option>';
		jQuery.each(RegionesYciudades.regiones, function () {
			if (RegionesYciudades.regiones[iRegiones].NombreRegion == valorRegion) {
				var iCiudades = 0;
				jQuery.each(RegionesYciudades.regiones[iRegiones].Ciudades, function () {
					htmlCiudad = htmlCiudad + '<option value="' + RegionesYciudades.regiones[iRegiones].Ciudades[iCiudades] + '">' + RegionesYciudades.regiones[iRegiones].Ciudades[iCiudades] + '</option>';
					iCiudades++;
				});
			}
			iRegiones++;
		});
		jQuery('#Ciudades').html(htmlCiudad);
	});
	jQuery('#Ciudades').change(function () {
		if (jQuery(this).val() == 'sin-region') {
			alert('selecciones Región');
		} else if (jQuery(this).val() == 'sin-ciudad') {
			alert('seleccione una Ciudad');
		}
	});
	jQuery('#regiones').change(function () {
		if (jQuery(this).val() == 'sin-region') {
			alert('selecciones Región');
		}
	});

});